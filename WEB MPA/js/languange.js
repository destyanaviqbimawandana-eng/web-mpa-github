let currentLang = localStorage.getItem("selectedLanguage") || "id";

function setActiveLanguage() {
    const t = translations[currentLang];
    
    // Update navbar text
    $("#institutionName").text(t.institutionName);
    $("#menuHome").text(t.menuHome);
    $("#menuProfile").text(t.menuProfile);
    $("#menuHistory").text(t.menuHistory);
    $("#menuVision").text(t.menuVision);
    $("#menuStructure").text(t.menuStructure);
    $("#menuCs").text(t.menuCs);
    $("#menuAbout").text(t.menuAbout);
    $("#logoutText").text(t.logoutText);
    
    if ($("#loginText").length && !localStorage.getItem("isLoggedIn")) {
        $("#loginText").text(t.loginText);
    }
    
    // Update footer
    $("#footerEmail").text(t.footerEmail);
    $("#footerPhone").text(t.footerPhone);
    
    // Update home page specific elements
    if ($("#welcomeTitle").length) $("#welcomeTitle").text(t.welcomeTitle);
    if ($("#globalSearchInput").length) $("#globalSearchInput").attr("placeholder", t.searchPlaceholder);
    if ($("#globalSearchBtn span").length) $("#globalSearchBtn span").text(t.searchBtn);
    if ($("#announcementTitle").length) $("#announcementTitle").text(t.announcementTitle);
    if ($("#announcementText").length) $("#announcementText").text(t.announcementText);
    if ($("#accredited").length) $("#accredited").text(t.accredited);
    if ($("#alumni").length) $("#alumni").text(t.alumni);
    if ($("#rating").length) $("#rating").text(t.rating);
    
    // Update login page specific elements
    if ($("#loginTitle").length) $("#loginTitle").text(t.loginTitle);
    if ($("#loginSubtitle").length) $("#loginSubtitle").text(t.loginSubtitle);
    if ($("#labelUsername").length) $("#labelUsername").text(t.username);
    if ($("#labelEmail").length) $("#labelEmail").text(t.email);
    if ($("#labelPassword").length) $("#labelPassword").text(t.password);
    if ($("#loginBtn").length) $("#loginBtn").html(`<i class="bi bi-box-arrow-in-right"></i> ${t.loginBtn}`);
    if ($("#loginDemo").length) $("#loginDemo").text(t.loginDemo);
    
    // Update customer service page
    if ($("#csTitle").length) $("#csTitle").text(t.csTitle);
    if ($("#csSubtitle").length) $("#csSubtitle").text(t.csSubtitle);
    if ($("#csHotline").length) $("#csHotline").text(t.csHotline);
    if ($("#csEmail").length) $("#csEmail").text(t.csEmail);
    if ($("#csHours").length) $("#csHours").text(t.csHours);
    if ($("#csHoursValue").length) $("#csHoursValue").text(t.csHoursValue);
    if ($("#csInfo").length) $("#csInfo").text(t.csInfo);
    if ($("#csEmergency").length) $("#csEmergency").text(t.csEmergency);
    if ($("#csLiveChat").length) $("#csLiveChat").text(t.csLiveChat);
    if ($("#csLiveChatText").length) $("#csLiveChatText").text(t.csLiveChatText);
    if ($("#csHelpCenter").length) $("#csHelpCenter").text(t.csHelpCenter);
    if ($("#csHelpText").length) $("#csHelpText").text(t.csHelpText);
    
    // Update about page
    if ($("#aboutTitle").length) $("#aboutTitle").text(t.aboutTitle);
    if ($("#aboutSubtitle").length) $("#aboutSubtitle").text(t.aboutSubtitle);
    if ($("#aboutText1").length) $("#aboutText1").text(t.aboutText1);
    if ($("#aboutText2").length) $("#aboutText2").text(t.aboutText2);
    if ($("#aboutMission").length) $("#aboutMission").text(t.aboutMission);
    if ($("#aboutMissionText").length) $("#aboutMissionText").text(t.aboutMissionText);
    if ($("#aboutIntegrity").length) $("#aboutIntegrity").text(t.aboutIntegrity);
    if ($("#aboutIntegrityText").length) $("#aboutIntegrityText").text(t.aboutIntegrityText);
    if ($("#aboutReach").length) $("#aboutReach").text(t.aboutReach);
    if ($("#aboutReachText").length) $("#aboutReachText").text(t.aboutReachText);
    if ($("#aboutVersion").length) $("#aboutVersion").text(t.aboutVersion);
    
    // Update sejarah page
    if ($("#sejarahTitle").length) $("#sejarahTitle").text(t.sejarahTitle);
    if ($("#sejarahText1").length) $("#sejarahText1").text(t.sejarahText1);
    if ($("#sejarahText2").length) $("#sejarahText2").text(t.sejarahText2);
    if ($("#sejarahText3").length) $("#sejarahText3").text(t.sejarahText3);
    if ($("#sejarahAward").length) $("#sejarahAward").text(t.sejarahAward);
    
    // Update visi misi page
    if ($("#visiTitle").length) $("#visiTitle").text(t.visiTitle);
    if ($("#misiTitle").length) $("#misiTitle").text(t.misiTitle);
    if ($("#visiText").length) $("#visiText").text(`"${t.visiText}"`);
    if ($("#misi1").length) $("#misi1").text(t.misi1);
    if ($("#misi2").length) $("#misi2").text(t.misi2);
    if ($("#misi3").length) $("#misi3").text(t.misi3);
    if ($("#misi4").length) $("#misi4").text(t.misi4);
    
    // Update struktur page
    if ($("#strukturTitle").length) $("#strukturTitle").text(t.strukturTitle);
    if ($("#strukturSubtitle").length) $("#strukturSubtitle").text(t.strukturSubtitle);
    if ($("#kepalaLembaga").length) $("#kepalaLembaga").text(t.kepalaLembaga);
    if ($("#wakilAkademik").length) $("#wakilAkademik").text(t.wakilAkademik);
    if ($("#wakilKemahasiswaan").length) $("#wakilKemahasiswaan").text(t.wakilKemahasiswaan);
    if ($("#kepalaKurikulum").length) $("#kepalaKurikulum").text(t.kepalaKurikulum);
    if ($("#kepalaHrd").length) $("#kepalaHrd").text(t.kepalaHrd);
    if ($("#kepalaKeuangan").length) $("#kepalaKeuangan").text(t.kepalaKeuangan);
    
    // Update active language style
    if (currentLang === "id") {
        $("#langId").addClass("active");
        $("#langEn").removeClass("active");
    } else {
        $("#langEn").addClass("active");
        $("#langId").removeClass("active");
    }
}

function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem("selectedLanguage", currentLang);
    setActiveLanguage();
}

$(document).ready(function() {
    setActiveLanguage();
    
    $("#langId").on("click", function() { changeLanguage("id"); });
    $("#langEn").on("click", function() { changeLanguage("en"); });
    
    // Hapus loading screen
    setTimeout(function() {
        $(".loader-wrapper").fadeOut(500);
    }, 1000);
});